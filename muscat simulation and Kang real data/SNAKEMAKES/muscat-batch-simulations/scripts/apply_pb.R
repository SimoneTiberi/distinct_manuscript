suppressMessages({
  library(limma)
  library(muscat)
  library(scater)
  library(sctransform)
  library(SingleCellExperiment)
})

apply_pb <- function(sce, pars, ds_only = TRUE) {
  t1 <- system.time({
    a <- pars$assay
    if (!ds_only) 
      assay(sce, a) <- switch(a, 
                              counts = counts(sce),
                              cpm = calculateCPM(counts(sce)),
                              logcounts = normalizeCounts(computeLibraryFactors(sce)),
                              vstresiduals = vst(counts(sce), show_progress = FALSE)$y)
    pb <- aggregateData(sce, a, fun = pars$fun, scale = pars$scale)
  })[[3]]
  t2 <- system.time({
      # get sample names:
      samples = sapply(metadata(sce)$experiment_info$sample_id,
                       function(x){ strsplit(x, ".", fixed = TRUE)[[1]][[1]]})

    # define the design matrix:
    group = factor(colData(pb)$group_id)
    batch = factor(colData(pb)$batch_id)
    design = model.matrix(~group + samples + batch)
    rownames(design) = colnames(pb) # design rownames should have sample names.
    res <- tryCatch(
      do.call(pbDS, c(
        # test the second column of the design matrix (group):
        list(pb = pb, design = design, coef = 2, 
             filter = "none", verbose = FALSE),
        pars[names(pars) %in% names(formals(pbDS))])),
      error = function(e) e)
    if (!inherits(res, "error"))
      res <- dplyr::bind_rows(res$table[[1]])
  })[[3]]
  list(rt = c(t1, t2), tbl = res)
}
